// Function to get all California colleges
export function getCaliforniaColleges(): CommunityCollege[] {
  return californiaColleges
}

// Function to search colleges by name
export function searchCollegesByName(searchTerm: string): CommunityCollege[] {
  const term = searchTerm.toLowerCase()
  return californiaColleges.filter(college => 
    college.name.toLowerCase().includes(term)
  )
}

// Function to get college by ID
export function getCollegeById(id: string): CommunityCollege | undefined {
  return californiaColleges.find(college => college.id === id)
}
